def generate_all_pdfs(df: pd.DataFrame, max_pdfs: int = 20):
    """Generate clinical PDF reports for up to max_pdfs patients."""
    print(f"\n[PDF] Generating clinical PDF reports (up to {max_pdfs}) ...")

    # Prioritise: 2 per subtype (correct predictions) + remaining
    priority = []
    for s in SUBTYPES:
        mask = (df["True_Subtype"] == s) & (df["Pred_Subtype"] == s)
        top  = df[mask].nlargest(3, "Confidence")
        for _, row in top.iterrows():
            priority.append(row)
    seen  = set(r["Patient_ID"] for r in priority)
    extra = [row for _, row in df.iterrows()
             if row["Patient_ID"] not in seen][:max(0, max_pdfs - len(priority))]
    all_rows = priority[:max_pdfs] + extra[:max(0, max_pdfs - len(priority))]

    generated = 0
    for row in all_rows[:max_pdfs]:
        pid     = row["Patient_ID"]
        subtype = row["Pred_Subtype"]
        fname   = f"{pid}_{subtype.replace('+','p')}_report.pdf"
        out_path = PDF_DIR / fname
        try:
            export_clinical_pdf(row, subtype, out_path)
            generated += 1
            if generated % 5 == 0:
                print(f"  {generated}/{min(len(all_rows), max_pdfs)} reports written ...")
        except Exception as exc:
            print(f"  Warning: {pid} failed -- {exc}")

    print(f"  Done. {generated} PDF reports saved to: {PDF_DIR}")


