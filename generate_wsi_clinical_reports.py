# 5. FIGURE 52 -- Simulated Grad-CAM on H&E Patches
# ─────────────────────────────────────────────────────────────────────────────

def fig52_gradcam_overlays(df: pd.DataFrame):
    print("[Fig 52] Generating simulated Grad-CAM overlays ...")
    # One representative patient per subtype
    rep_rows = {}
    for s in SUBTYPES:
        mask = (df["True_Subtype"] == s) & (df["Pred_Subtype"] == s)
        best = df[mask].nlargest(1, "Confidence")
        if not best.empty:
            rep_rows[s] = best.iloc[0]

    SHOW_PATCHES = 4

    fig = plt.figure(figsize=(26, 13))
    fig.patch.set_facecolor("#080c14")

    outer = gridspec.GridSpec(3, 1, figure=fig, hspace=0.10,
                              left=0.02, right=0.99,
                              top=0.91, bottom=0.02)

    for row_idx, subtype in enumerate(SUBTYPES):
        if subtype not in rep_rows:
            continue
        row   = rep_rows[subtype]
        pid   = row["Patient_ID"]
        color = S_COLOR[subtype]
        conf  = float(row["Confidence"])

        patches = get_patches(pid, n=SHOW_PATCHES, thumb=200)

        inner = gridspec.GridSpecFromSubplotSpec(
            3, SHOW_PATCHES + 1, subplot_spec=outer[row_idx],
            width_ratios=[0.22] + [1] * SHOW_PATCHES,
            wspace=0.04, hspace=0.06)

        # Left annotation column
        ax_ann = fig.add_subplot(inner[:, 0])
        ax_ann.set_facecolor("#0d1421")
        ax_ann.axis("off")
        ax_ann.text(0.5, 0.80, subtype,
                    transform=ax_ann.transAxes, ha="center", va="center",
                    color=color, fontsize=14, fontweight="bold")
        ax_ann.text(0.5, 0.58, f"Patient:\n{pid[:12]}",
                    transform=ax_ann.transAxes, ha="center", va="center",
                    color="white", fontsize=7.5, linespacing=1.4)
        ax_ann.text(0.5, 0.40, f"Confidence:\n{conf:.0%}",
                    transform=ax_ann.transAxes, ha="center", va="center",
                    color="#10b981" if conf > 0.8 else "#f59e0b",
                    fontsize=7.5, linespacing=1.4)
        # Row labels
        row_labels = ["Original\nH&E", "Activation\nMap (CAM)", "Overlay\nBlend"]
        for ri, lbl in enumerate(row_labels):
            ax_ann.text(0.5, 0.22 - ri * 0.10, lbl,
                        transform=ax_ann.transAxes, ha="center", va="center",
                        color="#94a3b8", fontsize=6.5, linespacing=1.3)
        for sp in ax_ann.spines.values():
            sp.set_edgecolor(color)
            sp.set_linewidth(1.5)
            sp.set_visible(True)

        for pi, patch in enumerate(patches[:SHOW_PATCHES]):
            seed_val = int(abs(hash(pid))) % 9999 + pi * 17
            heatmap, blended = _simulated_gradcam(patch, subtype, seed=seed_val)

            for layer_idx, img_data in enumerate([patch, heatmap, blended]):
                ax = fig.add_subplot(inner[layer_idx, pi + 1])
                ax.imshow(img_data, aspect="equal")
                ax.axis("off")
                for sp in ax.spines.values():
                    sp.set_edgecolor(color)
                    sp.set_linewidth(1.0)
                    sp.set_visible(True)
                if layer_idx == 0 and row_idx == 0:
                    ax.set_title(f"ROI #{pi+1}", color="#64748b",
                                 fontsize=7, pad=3)

    # Shared colour bar (JET)
    cax = fig.add_axes([0.91, 0.06, 0.012, 0.82])
    sm  = plt.cm.ScalarMappable(cmap="jet",
                                norm=plt.Normalize(vmin=0, vmax=1))
    sm.set_array([])
    cb  = plt.colorbar(sm, cax=cax)
    cb.set_label("CAM Activation", color="white", fontsize=9)
    cb.ax.yaxis.set_tick_params(color="white", labelsize=7)
    plt.setp(plt.getp(cb.ax.axes, "yticklabels"), color="white")

    fig.suptitle(
        "Simulated Class Activation Maps (Grad-CAM) on H&E Tissue Patches\n"
        "Red = high activation region  │  Blue = low activation  │  "
        "Overlay: 60% tissue + 40% CAM  │  One representative patient per subtype",
        color="white", fontsize=10.5, fontweight="bold", y=0.94)

    out = FIG_DIR / "52_wsi_gradcam_overlays.png"
    plt.savefig(out, dpi=140, bbox_inches="tight", facecolor="#080c14")
    plt.close()
    print(f"  Saved: {out}")


