# 3. FIGURE 50 -- H&E Tissue Subtype Gallery
# ─────────────────────────────────────────────────────────────────────────────

def fig50_he_subtype_gallery(df: pd.DataFrame):
    print("\n[Fig 50] Generating H&E subtype gallery ...")
    COLS = 6
    fig  = plt.figure(figsize=(26, 18))
    fig.patch.set_facecolor("#0a0e1a")

    outer = gridspec.GridSpec(3, 1, figure=fig, hspace=0.06,
                              left=0.06, right=0.99, top=0.93, bottom=0.02)

    for row_idx, subtype in enumerate(SUBTYPES):
        sub_df   = df[df["True_Subtype"] == subtype]
        pids     = sub_df["Patient_ID"].tolist()
        color    = S_COLOR[subtype]

        # Collect patches from up to COLS patients
        all_patches = []
        for pid in pids:
            pts = get_patches(pid, n=1, thumb=180)
            if pts:
                all_patches.append((pid, pts[0]))
            if len(all_patches) >= COLS:
                break
        while len(all_patches) < COLS:
            pid = f"SYNTH-{subtype}-{len(all_patches)}"
            all_patches.append((pid, _synthetic_he(pid, 1, 180)[0]))

        inner = gridspec.GridSpecFromSubplotSpec(
            1, COLS + 1, subplot_spec=outer[row_idx],
            width_ratios=[0.28] + [1] * COLS, wspace=0.06)

        # Left label panel
        ax_lbl = fig.add_subplot(inner[0])
        ax_lbl.set_facecolor("#0a0e1a")
        ax_lbl.axis("off")
        ax_lbl.text(0.5, 0.72, subtype,
                    transform=ax_lbl.transAxes, ha="center", va="center",
                    color=color, fontsize=18, fontweight="bold")
        ax_lbl.text(0.5, 0.30, SUBTYPE_MORPHOLOGY[subtype],
                    transform=ax_lbl.transAxes, ha="center", va="center",
                    color="#94a3b8", fontsize=6.8, linespacing=1.55,
                    multialignment="center")

        for col_idx, (pid, patch) in enumerate(all_patches[:COLS]):
            ax = fig.add_subplot(inner[col_idx + 1])
            ax.imshow(patch, aspect="equal")
            ax.axis("off")
            # Coloured frame
            for spine in ax.spines.values():
                spine.set_edgecolor(color)
                spine.set_linewidth(2.8)
                spine.set_visible(True)
            ax.set_title(f"{pid[:12]}..." if len(pid) > 12 else pid,
                         color="#64748b", fontsize=6.2, pad=3)

        # Subtype row annotation
        fig.text(0.005, 1 - (row_idx + 0.5) / 3,
                 subtype, ha="left", va="center", rotation=90,
                 color=color, fontsize=9, fontweight="bold",
                 transform=fig.transFigure)

    fig.suptitle(
        "H&E Histopathology Tissue Gallery -- TCGA-BRCA Whole-Slide Image Patches\n"
        "Real 512x512 px tissue sections from Formalin-Fixed Paraffin-Embedded (FFPE) sections │ "
        "Stained: Haematoxylin & Eosin",
        color="white", fontsize=11, fontweight="bold", y=0.97)

    out = FIG_DIR / "50_wsi_he_subtype_gallery.png"
    plt.savefig(out, dpi=150, bbox_inches="tight", facecolor="#0a0e1a")
    plt.close()
    print(f"  Saved: {out}")


