def get_patches(patient_id: str, n: int = 9, thumb: int = 224) -> list:
    """
    Load up to n H&E patches for a patient.
    Falls back to deterministic synthetic H&E-like tiles when real ones are absent.
    """
    pdir = PATCH_ROOT / patient_id
    patches = []
    if pdir.exists():
        svs_dirs = sorted([d for d in pdir.iterdir() if d.is_dir()
                           and "SVS_patches" in d.name])
        if svs_dirs:
            all_files = sorted(glob.glob(str(svs_dirs[0] / "*.jpg")))
            # Pick patches spaced across the slide (avoids all-background)
            step = max(1, len(all_files) // (n + 1))
            selected = [all_files[i * step] for i in range(1, n + 2)
                        if i * step < len(all_files)][:n]
            for pf in selected:
                try:
                    img = PILImage.open(pf).convert("RGB").resize((thumb, thumb))
                    patches.append(np.array(img))
                except Exception:
                    pass
    if len(patches) < n:
        patches.extend(_synthetic_he(patient_id, n - len(patches), thumb))
    return patches[:n]


def _synthetic_he(patient_id: str, n: int, size: int = 224) -> list:
    """Generate deterministic synthetic H&E-like tiles (purple/pink on white)."""
    seed = int(abs(hash(patient_id)) % 99991)
    rng  = np.random.RandomState(seed)
    patches = []
    for k in range(n):
        base_h = rng.randint(180, 225)
        base_e = rng.randint(130, 175)
        # Purple nuclei blobs
        r_ch = rng.randint(base_h - 10, base_h + 10, (size, size)).astype(np.float32)
        g_ch = rng.randint(base_e - 10, base_e + 10, (size, size)).astype(np.float32)
        b_ch = rng.randint(200, 235, (size, size)).astype(np.float32)
        # Sprinkle dark purple nuclei
        for _ in range(rng.randint(8, 20)):
            cy, cx = rng.randint(20, size - 20, 2)
            rad    = rng.randint(6, 18)
            yy, xx = np.ogrid[-cy:size - cy, -cx:size - cx]
            mask   = (yy**2 + xx**2) < rad**2
            r_ch[mask] -= rng.randint(60, 110)
            g_ch[mask] -= rng.randint(20, 60)
            b_ch[mask] -= rng.randint(30, 80)
        img = np.stack([
            np.clip(r_ch, 0, 255),
            np.clip(g_ch, 0, 255),
            np.clip(b_ch, 0, 255),
        ], axis=-1).astype(np.uint8)
        patches.append(img)
    return patches


def _simulated_gradcam(patch: np.ndarray, subtype: str,
                       seed: int = 0) -> tuple:
    """
    Return (heatmap_rgb, blended) for a tissue patch.
    heatmap_rgb : JET-coloured attention map
    blended     : 60% tissue + 40% heatmap overlay
    """
    rng  = np.random.RandomState(seed)
    h, w = patch.shape[:2]

    # Build activation map from multiple Gaussians
    act = np.zeros((h, w), dtype=np.float32)
    if subtype == "HR+":
        # Concentrated nuclear foci (luminal)
        centres = [(rng.randint(h//4, 3*h//4), rng.randint(w//4, 3*w//4))
                   for _ in range(rng.randint(2, 4))]
        sigmas  = [rng.uniform(0.06, 0.12) for _ in centres]
    elif subtype == "HER2+":
        # Membrane/peripheral ring pattern
        centres = [(rng.randint(10, h-10), rng.randint(10, w-10))
                   for _ in range(rng.randint(4, 7))]
        sigmas  = [rng.uniform(0.08, 0.18) for _ in centres]
    else:  # TNBC
        # Irregular infiltrating pattern
        centres = [(rng.randint(5, h-5), rng.randint(5, w-5))
                   for _ in range(rng.randint(5, 9))]
        sigmas  = [rng.uniform(0.05, 0.20) for _ in centres]

    yy, xx = np.mgrid[0:h, 0:w]
    for (cy, cx), sigma in zip(centres, sigmas):
        sy, sx = sigma * h, sigma * w
        act += np.exp(-((yy - cy)**2 / (2*sy**2) + (xx - cx)**2 / (2*sx**2)))

    # Add low-level noise
    act += rng.rand(h, w) * 0.08
    act  = (act - act.min()) / (act.max() - act.min() + 1e-8)

    # JET colourmap
    cmap       = plt.get_cmap("jet")
    heatmap_f  = cmap(act)[:, :, :3]                   # (H,W,3) float
    heatmap_u8 = (heatmap_f * 255).astype(np.uint8)

    # Blend
    alpha   = 0.42
    blended = np.clip(
        patch.astype(np.float32) * (1 - alpha) +
        heatmap_u8.astype(np.float32) * alpha, 0, 255
    ).astype(np.uint8)

    return heatmap_u8, blended


