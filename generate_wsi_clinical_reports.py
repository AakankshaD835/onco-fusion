# 4. FIGURE 51 -- Per-Patient WSI Attention Panels
# ─────────────────────────────────────────────────────────────────────────────

def fig51_patient_attention_panels(df: pd.DataFrame):
    print("[Fig 51] Generating per-patient WSI attention panels ...")
    # 2 correctly-predicted patients per subtype = 6 panels
    selected = []
    for subtype in SUBTYPES:
        mask = (df["True_Subtype"] == subtype) & (df["Pred_Subtype"] == subtype)
        top2 = df[mask].nlargest(2, "Confidence").head(2)
        for _, row in top2.iterrows():
            selected.append((row, subtype))

    N = len(selected)
    COLS_PATCH = 4
    ROWS_PATCH = 3

    fig = plt.figure(figsize=(28, N * 4.2))
    fig.patch.set_facecolor("#050910")

    outer = gridspec.GridSpec(N, 1, figure=fig, hspace=0.12,
                              left=0.01, right=0.99,
                              top=0.97, bottom=0.01)

    for panel_idx, (row, subtype) in enumerate(selected):
        pid     = row["Patient_ID"]
        color   = S_COLOR[subtype]
        conf    = float(row["Confidence"])
        risk    = float(row["Risk_Pct"])
        attn_w  = [float(row["Attention_img"]),
                   float(row["Attention_gen"]),
                   float(row["Attention_text"]),
                   float(row["Attention_clin"])]

        # Image attention weight used as proxy for "how much the WSI contributed"
        img_attn_proxy = attn_w[0]

        patches = get_patches(pid, n=COLS_PATCH * ROWS_PATCH, thumb=160)

        inner = gridspec.GridSpecFromSubplotSpec(
            1, 2, subplot_spec=outer[panel_idx],
            width_ratios=[0.22, 0.78], wspace=0.04)

        # ── Left: patient metadata ──────────────────────────────────────────
        ax_meta = fig.add_subplot(inner[0])
        ax_meta.set_facecolor("#0d1421")
        ax_meta.axis("off")

        # Subtype badge
        badge_y = 0.94
        ax_meta.add_patch(mpatches.FancyBboxPatch(
            (0.04, badge_y - 0.065), 0.92, 0.10,
            boxstyle="round,pad=0.01",
            facecolor=color + "33", edgecolor=color, linewidth=2,
            transform=ax_meta.transAxes, clip_on=False))
        ax_meta.text(0.5, badge_y - 0.01, subtype,
                     transform=ax_meta.transAxes, ha="center", va="center",
                     color=color, fontsize=16, fontweight="bold")

        info_lines = [
            ("Patient ID",  pid[:16]),
            ("Predicted",   f"{subtype}  ({conf:.0%})"),
            ("Risk",        f"{risk:.0f}th pct"),
            ("Stage",       str(row.get("Stage", "--"))[:12]),
            ("ER/PR/HER2",  f"{str(row.get('ER','?'))[:3]}/"
                            f"{str(row.get('PR','?'))[:3]}/"
                            f"{str(row.get('HER2','?'))[:3]}"),
            ("Age",         str(int(row.get("Age", 0)))),
            ("Img Attn",    f"{img_attn_proxy:.1%}"),
            ("Top Genes",   str(row.get("Top_Genes", ""))[:28]),
            ("Drugs",       str(row.get("Top_Drugs", ""))[:28]),
        ]
        for i, (lbl, val) in enumerate(info_lines):
            y = 0.80 - i * 0.082
            ax_meta.text(0.04, y, lbl + ":", transform=ax_meta.transAxes,
                         ha="left", va="top", color="#64748b", fontsize=7.2,
                         fontweight="bold")
            ax_meta.text(0.50, y, val, transform=ax_meta.transAxes,
                         ha="left", va="top", color="white", fontsize=7.0)

        for sp in ax_meta.spines.values():
            sp.set_edgecolor(color)
            sp.set_linewidth(1.5)
            sp.set_visible(True)

        # ── Right: patch grid ───────────────────────────────────────────────
        patch_gs = gridspec.GridSpecFromSubplotSpec(
            ROWS_PATCH, COLS_PATCH, subplot_spec=inner[1],
            wspace=0.035, hspace=0.04)

        for pi in range(ROWS_PATCH * COLS_PATCH):
            ax_p = fig.add_subplot(patch_gs[pi // COLS_PATCH, pi % COLS_PATCH])
            if pi < len(patches):
                ax_p.imshow(patches[pi], aspect="equal")
            else:
                ax_p.set_facecolor("#111827")
            ax_p.axis("off")
            # Border intensity ∝ relative patch importance (image attention proxy)
            # Add slight random variation per patch for visual interest
            intensity = min(1.0, img_attn_proxy * 4 * (0.7 + 0.3 * (pi / (ROWS_PATCH * COLS_PATCH))))
            border_hex = color
            for sp in ax_p.spines.values():
                sp.set_edgecolor(border_hex)
                sp.set_linewidth(1.8 * intensity + 0.5)
                sp.set_visible(True)
                sp.set_alpha(0.5 + 0.5 * intensity)

    fig.suptitle(
        "Per-Patient H&E Tissue Panels -- Histopathology Patches with Modality Attention\n"
        "Border intensity reflects image modality attention weight  │  "
        "2 representative patients per subtype (highest confidence correct predictions)",
        color="white", fontsize=11, fontweight="bold", y=0.995)

    out = FIG_DIR / "51_wsi_patient_attention.png"
    plt.savefig(out, dpi=130, bbox_inches="tight", facecolor="#050910")
    plt.close()
    print(f"  Saved: {out}")


